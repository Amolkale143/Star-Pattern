package Logical;

public class Swaping_Of_number 
{
	public static void main(String[] args)
	{
		
		int a=10;   //20
		
		int b=20;   //10
		
		
		       a=a+b;//30
		
		       b=a-b;//10
		       
		       a=a-b;//20
		       
		       System.out.println(a); //20
		       System.out.println(b); //10
		
	}

}
